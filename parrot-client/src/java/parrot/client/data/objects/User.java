package parrot.client.data.objects;

public class User {
	public long id;
	public String login;
	public String password;
	public String name;

	public long getId() {
		return id;
	}
	public String getLogin() {
		return login;
	}
	public String getPassword() {
		return password;
	}
	public String getName() {
		return name;
	}
}
